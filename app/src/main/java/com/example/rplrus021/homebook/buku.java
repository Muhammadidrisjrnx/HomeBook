package com.example.rplrus021.homebook;

public class buku {
    private String id_buku;
    private String nama_gambar;
    private String judul_buku;
    private String id_penulis;
    private String halaman_buku;
    private String id_penerbit;
    private String tahun_terbit;
    private String description;
    private String tipe_buku;

    public String getId_buku() {
        return id_buku;
    }

    public void setId_buku(String id_buku) {
        this.id_buku = id_buku;
    }

    public String getNama_gambar() {
        return nama_gambar;
    }

    public void setNama_gambar(String nama_gambar) {
        this.nama_gambar = nama_gambar;
    }

    public String getJudul_buku() {
        return judul_buku;
    }

    public void setJudul_buku(String judul_buku) {
        this.judul_buku = judul_buku;
    }

    public String getId_penulis() {
        return id_penulis;
    }

    public void setId_penulis(String id_penulis) {
        this.id_penulis = id_penulis;
    }

    public String getHalaman_buku() {
        return halaman_buku;
    }

    public void setHalaman_buku(String halaman_buku) {
        this.halaman_buku = halaman_buku;
    }

    public String getId_penerbit() {
        return id_penerbit;
    }

    public void setId_penerbit(String id_penerbit) {
        this.id_penerbit = id_penerbit;
    }

    public String getTahun_terbit() {
        return tahun_terbit;
    }

    public void setTahun_terbit(String tahun_terbit) {
        this.tahun_terbit = tahun_terbit;
    }


    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getTipe_buku() {
        return tipe_buku;
    }

    public void setTipe_buku(String tipe_buku) {
        this.tipe_buku = tipe_buku;
    }
}
